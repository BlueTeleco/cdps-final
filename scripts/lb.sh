#!/bin/bash

############################################################################################################################
#
#									 Configure load balancer
#
############################################################################################################################

. tools.sh

servs=(1 2 3)

back=""
for n in ${servs[*]}
do
	back="$back --backend 10.1.3.1$n:3030"
done

send lb "
	xr --verbose --server tcp:0:80 $back --web-interface 0:8001
"
